export function getTranslationsForLanguage(code: Language): TranslationSet {
  // Check direct base translation
  if (code in BASE_TRANSLATIONS) {
    return BASE_TRANSLATIONS[code as keyof typeof BASE_TRANSLATIONS];
  }
  
  // Check extra translations
  if (code in EXTRA_TRANSLATIONS) {
    return EXTRA_TRANSLATIONS[code as keyof typeof EXTRA_TRANSLATIONS];
  }
  
  // Custom overrides
  if (code === 'AZ') {
    return {
      ...BASE_TRANSLATIONS.TR,
      ...AZ_TRANSLATION_OVERRIDE
    };
  }

  // Fallback map for similar/regional dialects to ensure pure local codification 
  // without default raw English fallback if possible
  switch (code) {
    // Slavyan languages fallback to RU if preferred or EN
    case 'UK':
    case 'BG':
    case 'PL':
    case 'CS':
      return BASE_TRANSLATIONS.RU; // High degree of localized comfort for nearby regions
    // Nordic languages fallback to SV
    case 'NO':
    case 'DA':
    case 'FI':
      return BASE_TRANSLATIONS.SV;
    // Latin languages fallback to ES or FR or IT
    case 'PT':
    case 'RO':
      return BASE_TRANSLATIONS.PT; // Portuguese is fully defined in the PT fallback if needed or we use PT directly
    default:
      return BASE_TRANSLATIONS.EN;
  }
}
